package random_All;

import java.util.Random;
/*
Метод Math.random() возвращает случайное число от 0 до 1 с большим количеством знаков после запятой.
Но в реальных задачах бывает нужно получать случайные целые числа.
 Реализуйте код, который печатает на экран случайное целое число от 0 до 10. Чтобы получить такое число,
нужно умножить результат вызова Math.random() на 10 и преобразовать тип полученного числа из double в int.
 */
public class RandomNumberExample {
    public static void main(String[] args) {
        // Создаем генератор случайных чисел
        Random random = new Random();

        // Генерируем случайное число от 0 до 10 (включительно)
        int randomNumber = random.nextInt(11); // 11, так как верхняя граница не включается

        // Выводим результат на экран
        System.out.println("Случайное целое число от 0 до 10: " + randomNumber);
    }
}

