package lesson_24_Type_Erasure;

//Использование enum
// Объяснение
//Поля mass и radius: Описывают массу и радиус планеты соответственно.
//Конструктор Planet: Устанавливает значения полей mass и radius.
//Методы getMass и getRadius: Возвращают массу и радиус планеты.
//Результат выполнения
//Когда вы выполните программу, она выведет информацию о каждой планете,
// включая её массу и радиус. Пример вывода:
//Planet: MERCURY
//Mass: 3.30e+23 kg
//Radius: 2.44e+06 m
//
//Planet: VENUS
//Mass: 4.87e+24 kg
//Radius: 6.05e+06 m
//
//Planet: EARTH
//Mass: 5.98e+24 kg
//Radius: 6.38e+06 m
//Этот пример демонстрирует использование enum для моделирования планет
// с их радиусами и массами. Это упрощенный вариант,
// который оставляет только необходимые поля и методы доступа,
// что делает код более читаемым и легким для понимания.
//
//Резюме
//Стирание типов: Generics в Java реализованы через стирание типов, что означает,
// что информация о типах удаляется во время компиляции,
// и все типы параметров заменяются их границами или Object.
//Generic методы: Позволяют создавать методы, которые могут работать с любыми типами данных.
//Enum: Представляют фиксированный набор констант и могут содержать поля, методы и конструкторы.


public class Main {
    public static void main(String[] args) {
        for (Planet planet : Planet.values()) {
            System.out.printf("Planet: %s%n", planet);
            System.out.printf("Mass: %.2e kg%n", planet.getMass());
            System.out.printf("Radius: %.2e m%n", planet.getRadius());
            System.out.println();
        }
    }
}